package kr.project.sph.dto;

public class MailReceiveVO {

	private int receiveNum;
	private int mnno;
	private int state;
	private int mno;
	private int receiveVcnt;

	public int getReceiveNum() {
		return receiveNum;
	}

	public void setReceiveNum(int receiveNum) {
		this.receiveNum = receiveNum;
	}

	public int getMnno() {
		return mnno;
	}

	public void setMnno(int mnno) {
		this.mnno = mnno;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public int getMno() {
		return mno;
	}

	public void setMno(int mno) {
		this.mno = mno;
	}

	public int getReceiveVcnt() {
		return receiveVcnt;
	}

	public void setReceiveVcnt(int receiveVcnt) {
		this.receiveVcnt = receiveVcnt;
	}

}
