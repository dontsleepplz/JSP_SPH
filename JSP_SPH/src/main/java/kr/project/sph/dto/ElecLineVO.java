package kr.project.sph.dto;

public class ElecLineVO {

	private int eno;
	private int sno;
	private int lank;
	private int status;
	private int mno;

	public int getEno() {
		return eno;
	}

	public void setEno(int eno) {
		this.eno = eno;
	}

	public int getSno() {
		return sno;
	}

	public void setSno(int sno) {
		this.sno = sno;
	}

	public int getLank() {
		return lank;
	}

	public void setLank(int lank) {
		this.lank = lank;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getMno() {
		return mno;
	}

	public void setMno(int mno) {
		this.mno = mno;
	}

}
