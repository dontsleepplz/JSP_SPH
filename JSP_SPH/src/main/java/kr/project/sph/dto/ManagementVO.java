package kr.project.sph.dto;

public class ManagementVO {

	private int manageNum;
	private int cno;
	private int managePay;

	public int getManageNum() {
		return manageNum;
	}

	public void setManageNum(int manageNum) {
		this.manageNum = manageNum;
	}

	public int getCno() {
		return cno;
	}

	public void setCno(int cno) {
		this.cno = cno;
	}

	public int getManagePay() {
		return managePay;
	}

	public void setManagePay(int managePay) {
		this.managePay = managePay;
	}

}
