public class OperationteamVO {

	private int otno;
	private String role;
	private int ono;
	private int mno;

	public int getOtno() {
		return otno;
	}

	public void setOtno(int otno) {
		this.otno = otno;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public int getOno() {
		return ono;
	}

	public void setOno(int ono) {
		this.ono = ono;
	}

	public int getMno() {
		return mno;
	}

	public void setMno(int mno) {
		this.mno = mno;
	}

}
